const createEmptyMunicipalityRow = () => ({
    id: null,
    istatCode: '',
    cadastralCode: '',
    municipality: '',
    province: '',
    region: '',
    postalCode: ''
});

const buildPayloadReport = payload => {
    const inserted = payload.changes.inserted.length;
    const updated = payload.changes.updated.length;
    const deleted = payload.changes.deleted.length;

    return [
        'Multifield lookup payload',
        '',
        `Rows ready for save: ${inserted + updated + deleted}`,
        `Inserted rows: ${inserted}`,
        `Updated rows: ${updated}`,
        `Deleted rows: ${deleted}`,
        '',
        'The JSON tab shows the backend-ready CRUD payload generated by AMB Grid.'
    ];
};

const restoreLookupOriginFocus = cell => {
    globalThis.requestAnimationFrame(() => {
        const element = cell && cell.getElement && cell.getElement();

        if (!element || typeof element.focus !== 'function') return;

        try {
            element.focus({ preventScroll: true });
        } catch {
            element.focus();
        }
    });
};

const loadMunicipalities = async () => {
    const response = await fetch(DATASET_URL);

    if (!response.ok) {
        throw new Error(`Unable to load the municipality demo dataset (${response.status})`);
    }

    return response.json();
};

export default async function multifieldLookup(app) {
    app.innerHTML = `
        <h2>Italian municipality multifield lookup</h2>
        <p class="demo-note">
            Click the Municipality cell to open the lookup. The municipality
            cannot be typed manually in this demo: selecting a valid record
            keeps Municipality, Province, Region, Postal Code, ISTAT Code and
            Cadastral Code synchronized.
        </p>
        <details class="demo-disclosure">
            <summary class="demo-disclosure__summary">Multifield lookup behavior</summary>
            <div class="demo-disclosure__content">
                <ul class="demo-rules-list">
                    <li>The Municipality cell opens the lookup.</li>
                    <li>Selecting a municipality fills province, region, postal code, ISTAT code, and cadastral code.</li>
                    <li>Derived cells are intentionally not edited directly.</li>
                    <li>Actionable readonly cells look different from passive derived cells.</li>
                    <li>The payload shows the CRUD data ready for backend integration.</li>
                </ul>
            </div>
        </details>
        <p class="demo-warning"><strong>Demo data warning:</strong> ${DATASET_WARNING}</p>
        <div id="municipality-table"></div>
    `;

    const tableMount = app.querySelector('#municipality-table');
    let municipalities;

    try {
        municipalities = await loadMunicipalities();
    } catch (error) {
        tableMount.textContent = error.message;
        return null;
    }

    const municipalityLookup = AMB.lookup({
        keyField: 'istatCode',
        valueField: 'istatCode',
        labelField: 'municipalityName',
        columns: MUNICIPALITY_LOOKUP_COLUMNS,
        search: {
            fields: 'visible'
        },
        mapToRow: MUNICIPALITY_MAP_TO_ROW,
        load: () => municipalities
    });
    const lookupDialog = new AMB.LookupDialog();
    let grid = null;
    let pendingLookupTimer = null;
    let pendingLookupCell = null;
    let pendingLookupRow = null;
    let activeLookupRow = null;
    let destroyed = false;
    const reportDialog = createDemoReportDialog();

    const openMunicipalityLookup = async cell => {
        if (!grid || destroyed) return;

        const row = cell.getRow();
        const rowData = row.getData();
        const stateField = grid.crud.options.stateField;

        if (rowData[stateField] === 'deleted') return;

        const visibleColumns = municipalityLookup.columns.filter(column => {
            return column.visible === true;
        });
        const selected = await lookupDialog.open({
            title: 'Select an Italian municipality',
            columns: visibleColumns,
            data: await municipalityLookup.load(),
            closeOnBackdropClick: false,
            pagination: {
                enabled: true,
                pageSize: 100,
                controls: 'full'
            },
            valueField: municipalityLookup.keyField,
            searchFields: visibleColumns.map(column => column.field),
            searchPlaceholder: 'Search municipality, province, region, or postal code...'
        });

        if (destroyed) return;

        const applied = applyMunicipalitySelection({
            selected,
            rowData,
            crud: grid.crud
        });

        if (applied) {
            restoreLookupOriginFocus(cell);
        }
    };

    const scheduleMunicipalityLookup = cell => {
        if (!grid || destroyed || pendingLookupTimer || activeLookupRow) return;

        const row = cell.getRow();
        const rowData = row.getData();

        if (rowData[grid.crud.options.stateField] === 'deleted') return;

        // Set the pending guard before the delay so a second click from a
        // double-click cannot schedule or interact with another dialog.
        pendingLookupCell = cell;
        pendingLookupRow = row;
        pendingLookupTimer = globalThis.setTimeout(async () => {
            const scheduledCell = pendingLookupCell;

            pendingLookupTimer = null;
            pendingLookupCell = null;
            activeLookupRow = pendingLookupRow;
            pendingLookupRow = null;

            if (destroyed || !scheduledCell) {
                activeLookupRow = null;
                return;
            }

            try {
                await openMunicipalityLookup(scheduledCell);
            } finally {
                activeLookupRow = null;
            }
        }, LOOKUP_OPEN_DELAY);
    };

    grid = AMB.table({
        selector: '#municipality-table',
        data: createInitialData(),
        toolbar: {
            buttons: [
                'add',
                'payload',
                {
                    id: 'reset-data',
                    label: 'Reset data',
                    title: 'Reset municipality demo data',
                    icon: demoIcon('reset'),
                    onClick: handleResetData
                }
            ],
            onAdd: handleAddRow,
            onPayload: handleShowPayload
        },
        layout: 'fitColumns',
        deleteColumn: {
            enabled: true,
            icons: demoDeleteColumnIcons
        },
        columns: [
            {
                title: 'Municipality',
                field: 'municipality',
                width: 220,
                required: true,
                editable: false,
                cssClass: 'amb-cell--readonly-actionable amb-cell--actionable',
                cellClick: (event, cell) => {
                    event.preventDefault?.();
                    scheduleMunicipalityLookup(cell);
                },
                cellDblClick: event => {
                    event.preventDefault?.();
                    event.stopPropagation?.();
                }
            },
            {
                title: 'Province',
                field: 'province',
                required: true,
                width: 100,
                editable: false,
                cssClass: 'amb-cell--readonly-passive amb-cell--derived'
            },
            {
                title: 'Region',
                field: 'region',
                width: 130,
                editable: false,
                cssClass: 'amb-cell--readonly-passive amb-cell--derived'
            },
            {
                title: 'Postal Code',
                field: 'postalCode',
                width: 125,
                editable: false,
                cssClass: 'amb-cell--readonly-passive amb-cell--derived'
            },
            {
                title: 'ISTAT Code',
                field: 'istatCode',
                width: 120,
                editable: false,
                cssClass: 'amb-cell--readonly-passive amb-cell--derived'
            },
            {
                title: 'Cadastral Code',
                field: 'cadastralCode',
                width: 155,
                editable: false,
                cssClass: 'amb-cell--readonly-passive amb-cell--derived'
            }
        ]
    });

    function handleAddRow() {
        grid.feedback.clear();
        grid.crud.addRow(createEmptyMunicipalityRow());
    }

    async function handleResetData() {
        grid.feedback.clear();
        await grid.table.setData(createInitialData());
        grid.feedback.show({
            type: 'success',
            message: 'Municipality demo data reset.'
        });
    }

    function handleShowPayload({ payload }) {
        reportDialog.open({
            title: 'Multifield lookup payload',
            reportLines: buildPayloadReport(payload),
            jsonData: payload
        });
    }

    return {
        ...grid,
        destroy() {
            destroyed = true;
            reportDialog.destroy();

            if (pendingLookupTimer) {
                globalThis.clearTimeout(pendingLookupTimer);
                pendingLookupTimer = null;
            }

            pendingLookupCell = null;
            pendingLookupRow = null;

            if (lookupDialog.resolve) {
                lookupDialog.close(null);
            } else {
                lookupDialog.destroy();
            }

            grid.destroy();
        }
    };
}
