/**
 * Creates the runtime cell-state reading methods exposed by the AMB Grid
 * controller.
 *
 * @param {object} context - Required method dependencies.
 * @param {object} context.table - Grid table instance.
 * @returns {object} Cell-state reading methods for the flat controller API.
 * @private
 * @internal
 */
export const createCellStateMethods = ({ table }) => ({
    /**
     * Clears the grid's native edited marker from one or more cells.
     *
     * Pass a cell component, an array of cell components, or omit the argument
     * to clear all cells currently marked as edited by the grid.
     *
     * Clearing the native marker does not restore previous values and does not
     * reset AMB Grid row state, snapshots or save-payload information.
     *
     * Native editing and validation events generated by the grid remain under
     * the control of the runtime engine.
     *
     * @param {object|object[]} [cells] - Cell component or cells to clear.
     * @returns {void}
     */
    clearCellEdited(...args) {
        return table.clearCellEdited(...args);
    },

    /**
     * Clears the grid's native validation marker from one or more cells.
     *
     * Pass a cell component, an array of cell components, or omit the argument
     * to clear all cells currently marked as invalid by the grid.
     *
     * The operation does not run a new validation pass and does not clear AMB
     * Grid `_errors`, validation messages or application-level row state.
     *
     * @param {object|object[]} [cells] - Cell component or cells to clear.
     * @returns {void}
     */
    clearCellValidation(...args) {
        return table.clearCellValidation(...args);
    },

    /**
     * Returns the cell components currently marked as edited by the grid.
     *
     * This native editing list is separate from AMB Grid row state, snapshots
     * and save payload generation. The returned array and cell components
     * retain their runtime identities and should be treated carefully.
     *
     * @returns {object[]} Cell components currently marked as edited.
     */
    getEditedCells() {
        return table.getEditedCells();
    },

    /**
     * Returns the cell components currently marked as invalid by the grid's
     * native validation state.
     *
     * This list is separate from AMB Grid validation errors and `_errors`
     * metadata. The method reads the current native state without triggering a
     * new validation pass.
     *
     * @returns {object[]} Cell components currently marked as invalid.
     */
    getInvalidCells() {
        return table.getInvalidCells();
    }
});
